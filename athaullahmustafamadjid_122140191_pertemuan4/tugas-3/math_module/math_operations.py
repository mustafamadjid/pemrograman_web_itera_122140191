#Geometri
PI = 3.14
def luas_persegi(n):
    return n * n

def keliling_persegi(n):
    return 4 * n

def luas_persegi_panjang(a, b):
    return a * b

def keliling_persegi_panjang(a, b):
    return 2 * (a + b)

def luas_lingkaran(r):
    return PI * r * r

def keliling_lingkaran(r):
    return 2 * PI * r




#konversi suhu
def celcius_to_fahrenheit(celcius):
    return celcius * 1.8 + 32

def fahrenheit_to_celcius(fahrenheit):
    return (fahrenheit - 32) / 1.8

def celcius_to_kelvin(celcius):
    return celcius + 273

def celcius_to_reamur(celcius):
    return celcius * 0.8


